import mongoose from 'mongoose';

const contact = mongoose.Schema({
    name: {
        type: String,
        required: [true, 'Set name for contact'],
    },
    email: {
        type: String,
    },
    phone: {
        type: String,
    },
    favorite: {
        type: Boolean,
        default: false,
    },
    timestamps: true,
});

export default mongoose.model('Contact', contact);
